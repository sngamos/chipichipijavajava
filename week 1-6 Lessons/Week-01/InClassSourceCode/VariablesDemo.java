package week1;

import java.util.Arrays;

public class VariablesDemo {
    public static void main(String[] args) {
        int a = 1;
        boolean b = true;
        double c = 4.3;
        float d = 2.9f;
        char e = 'e';

        System.out.print(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}